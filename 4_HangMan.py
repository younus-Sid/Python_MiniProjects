def hangman():
    life = 6
    guessword = random.choice(words)
    guessword_letters = set(guessword)
    used_letters = set()
    answer = "_"*len(guessword)
    print(answer)
    while guessword != answer and life > 0:
        user_letter = input("Guess the letter: ").lower()
        if user_letter in used_letters:
            print("Letter already used. Try another")
        elif user_letter in guessword_letters:
            print("Right guess.")
            for i in range(len(guessword)):
                if user_letter == guessword[i]:
                    answer = answer[:i] + user_letter + answer[i+1:]
        elif user_letter.isalpha():
            life -= 1
            print(f"Wrong guess, {life} lives left.")
        else:
            print("Invalid guess.")
        if user_letter not in used_letters:
            used_letters.add(user_letter)
        print(answer)
        print(Hangman_Stages[6-life])
    if guessword != answer:
        print(f"You died. The word was '{guessword}'")
    else:
        print(Hangman_Stages[6])
        print("Congratulations! You Won.")
        
hangman()

